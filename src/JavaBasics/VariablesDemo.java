package JavaBasics;

public class VariablesDemo {
    static int intVar = 26;
    static boolean boolVar = true;
    static char charVar = 'C';
    static float floatVar = 12;
    static double doubleVar = 12.52;

    public static void main(String[] args) {



        System.out.println("Integer intVar = " + intVar + "\n Boolean boolVar = " + boolVar+ "\n Character charVar = " + charVar +"\n Float floatVar = "+ floatVar +"\n Double doubleVar = "+doubleVar );
    }

}